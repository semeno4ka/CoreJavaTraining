    /*
    OOP - Object Oriented Programming
    Java is OOP, because ALMOST everything in Java is treated as an object.
    But it is not 100% OOP. Because:
     1) primitives are not objects
     2) statics belong to Class, not to objects.
     3) enumeration(custom data type as primitives))

    There are 4 OOP:
    - Encapsulation
    - Inheritance
    - Abstraction
    - Polymorphism

    OOP: A design pattern of the application in an organized and modular way

    Encapsulation:
    HIDING THE DATA
    An object hides its internal data from code that's outside the class

    Hide an instance variable giving private access modifier, and making the methods that access those fields - public in order to access it out of the class
    These public methods are called GETTERS and SETTERS(accessor and mutator)
                                                     (get data and change data )

    FOR INSTANCE variables it is better to keep access PRIVATE

     */
