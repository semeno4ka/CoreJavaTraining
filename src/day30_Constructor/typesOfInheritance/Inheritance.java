    /*
                            Types of Inheritance in OOP Languages:

    Single Inheritance - parent->child (when parent has one child)

    Hierarchical Inheritance - parent -> child 1 (one parent has multiple child classes)
                                      -> child 2 etc

    MultiLevel Inheritance - parent -> child -> grandchild (child class having child class)

    Multiple Inheritance - parent 1 - parent 2 -> child (inherits from both parents. NOT ALLOWED IN JAVA with EXTENDS keyword)

     Field Hiding:
     whenever you have same name of variables in parent class and child class, the parent class varible will be hiddend(not inherited) and local child class variable will be seen
     */
